# # random.choice override

# Python 2 and 3 have a different implementation of random.choice,
# leading to different behaviour of the random generator. To ensure that
# the result are the same on both Python versions, we override the random.choice
# function with python 2.7's implementation.
# Let's note that this override will only hold for reasonable use of the random
# module, and does not affect random.Random() instances created by the user.

import random

def choice(seq):
    """Choose a random element from a non-empty sequence."""
    return seq[int(random.random() * len(seq))]  # raises IndexError if seq is empty

random.choice = choice

